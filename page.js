// for secound year subs 
let isFirstYear = true; // Variable to track if it's the first year or second year

document.getElementById('changeSubjectBtn').addEventListener('click', function () {
    const subjectDropdown = document.getElementById('subject');

    if (isFirstYear) {
        // Switch to second-year subjects
        document.querySelector('span').innerHTML = 'Second Year &#8595;:-';
        subjectDropdown.innerHTML = ''; // Clear the existing options
        const subjects = [
            { value: "_BLANK", text: "SELECT" },
            { value: "BCS301", text: "BCS301(DS)" },
            { value: "BCS302", text: "BCS302 (COA)" },
            { value: "BCS303", text: "BCS303 (DSTL)" },
            { value: "BAS303", text: "BAS303 (MATHS-4 (QUANTUM ONLY))" },
            { value: "BAS301", text: "BAS301 (TC)" },
            { value: "BCC301", text: "BCC301 (CS)" },
            // Add your second-year subjects here
        ];
        subjects.forEach(subject => {
            const option = document.createElement('option');
            option.value = subject.value;
            option.textContent = subject.text;
            subjectDropdown.appendChild(option);
        });
    } else {
        // Switch back to first-year subjects
        document.querySelector('span').innerHTML = 'First Year &#8595;:-';
        subjectDropdown.innerHTML = ''; // Clear the existing options
        const subjects = [
            { value: "_BLANCK", text: "SELECT" },
            { value: "BAS201", text: "BAS201 (Physics)" },
            { value: "BAS202", text: "BAS202 (Chemistry)" },
            { value: "BAS203", text: "BAS203 (Maths-2)" },
            { value: "BAS204", text: "BAS204 (Environment)" },
            { value: "BAS205", text: "BAS205 (SOFT SKILLS)" },
            { value: "BCS201", text: "BCS201 (C LANG.)" },
            { value: "BEC201", text: "BEC201 (ELECTRONIC)" },
            { value: "BEE201", text: "BEE201 (ELECTRICAL)" },
            { value: "BME201", text: "BME201 (MECHANICAL)" },
        ];
        subjects.forEach(subject => {
            const option = document.createElement('option');
            option.value = subject.value;
            option.textContent = subject.text;
            subjectDropdown.appendChild(option);
        });
    }

    isFirstYear = !isFirstYear; // Toggle between first-year and second-year
});
